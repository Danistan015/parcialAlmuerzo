    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps = null;
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "LLene los campos");

        } else {
            try {

                Conexion_db objCon = new Conexion_db();
                Connection conn = objCon.getConexion();

                int fila = almuerzo.getSelectedRow();
                String codigo = almuerzo.getValueAt(fila, 0).toString();

                ps = conn.prepareStatement("DELETE FROM almuerzo WHERE Id=?");
                ps.setString(1, codigo);
                ps.execute();

                modelo.removeRow(fila);
                JOptionPane.showMessageDialog(null, "Almuerzo Eliminado");
                limpiarCampos();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al Eliminar el Almuerzo");
                System.out.println(ex.toString());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String campo = txtId.getText();
        String where = "";
        if (!"".equals(campo)) {
            where = " WHERE Id = '" + campo + "'";
        }
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "LLene los campos");

        } else {

            try {
                almuerzo.setModel(modelo);
                PreparedStatement ps = null;
                ResultSet rs = null;
                Conexion_db conn = new Conexion_db();
                Connection con = conn.getConexion();

                String sql = "SELECT * FROM almuerzo" + where;
                ps = con.prepareStatement(sql);//consulta de la base de datos
                rs = ps.executeQuery();// resultado de la consulta
                ResultSetMetaData rsMd = rs.getMetaData();
                int cantidadColumnas = rsMd.getColumnCount();

                modelo.addColumn("Id");
                modelo.addColumn("Nombre");
                modelo.addColumn("precio");
                modelo.addColumn("EntradaAlmuerzo");
                modelo.addColumn("CentroAlmuerzo");
                modelo.addColumn("tipoPostre");
                modelo.addColumn("tipoJugo");

                int[] anchos = {120, 170, 170, 170, 150, 170, 170};
                for (int i = 0; i < modelo.getColumnCount(); i++) {
                    almuerzo.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                }

                while (rs.next()) {
                    Object[] filas = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        filas[i] = rs.getObject(i + 1);
                    }
                    modelo.addRow(filas);

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

